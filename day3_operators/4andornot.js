let alert1 = true;
let alert2 = false; 

// AND operator (&&)
console.log(alert1 && alert2); // false, because one operand is false

// OR operator (||)
console.log(alert1 || alert2); // true, because one operand is true

// NOT operator (!)
console.log(!alert1); // false, because alert1 is true
console.log(!alert2); // true, because alert2 is false