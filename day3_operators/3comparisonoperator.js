let x = "10";
let y = 10;

// == operator (loose equality)
console.log(x == y); // true, because values are equal after type coercion

// === operator (strict equality)
console.log(x === y); // false, because types are different

// != operator (loose inequality)
console.log(x != y); // false, because values are equal after type coercion

// !== operator (strict inequality)
console.log(x !== y); // true, because types are different